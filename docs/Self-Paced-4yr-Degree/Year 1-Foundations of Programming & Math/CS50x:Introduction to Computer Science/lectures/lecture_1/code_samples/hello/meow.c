// Using while loop to print "meow" three times

#include <stdio.h>

int main(void)
{
        // Declare number of meows
        int i = 3;

        // Loop a number of times
        while (i > 0)
        {
                printf("meow\n");
                i --;
        }

        // Exit successfully
        return 0;
}

/*
Steps:
        1. Create an int called i and assign the value 3
        2. Create a while loop that will run as long as i > 0
        3. Decrement the value of i
*/

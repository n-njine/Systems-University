// Print the values of i incrementally
/*
Steps:
        1. Create an int called i and assign the value 0.
        2. Create a while loop that will run as long as i < 3.
        3. Increment the value of i
*/

#include <stdio.h>

int main(void)
{
        int i = 0;

        while (i < 3)
        {
                printf("meow\n");
                i ++;
        }

        // Exit successfully
        return 0;
}
        

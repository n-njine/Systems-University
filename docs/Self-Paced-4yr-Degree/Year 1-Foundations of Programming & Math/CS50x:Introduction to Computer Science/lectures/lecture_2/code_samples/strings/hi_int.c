// Print ASCII representation of HI!

#include <stdio.h>

int main(void)
{
        // Declare chars
        char c1 = 'H';
        char c2 = 'I';
        char c3 = '!';

        // Print ASCII representation 
        printf("%i %i %i\n", c1, c2, c3);

        // Exit successfully
        return 0;
}

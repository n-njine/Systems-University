// Avarages three (hardcoded) numbers

#include <stdio.h>

int main(void)
{
        // Declare three scores, 72,73,33
        int score1 = 72;
        int score2 = 73;
        int score3 = 33;

        // Print average
        printf("Average: %f\n", (score1 + score2 + score3) / 3.0);

        // Exit successfully
        return 0;
}
